# Criando uma tabela hash (dict) em Python
tabela_hash = {}

# Inserindo chaves e valores
tabela_hash['nome'] = 'Alice'
tabela_hash['idade'] = 25
tabela_hash['sexo'] = 'Feminino'

# Acessando valores pela chave
print(tabela_hash['nome'])  # Saída: Alice
print(tabela_hash['idade'])  # Saída: 25
print(tabela_hash['sexo'])

# Verificando se uma chave existe
print('ano da morte' in tabela_hash)  # Saída: True